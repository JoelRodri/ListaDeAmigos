package com.company;

public class Nota {
    String nombre;
    String apellidos;
    String telefono;
    String correo;
    String discord;

    public Nota(String nombre, String apellidos, String telefono, String correo, String discord){
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.telefono = telefono;
        this.correo = correo;
        this.discord = discord;
    }
}
